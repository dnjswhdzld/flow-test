package com.dnjswhdzld.flow.vo;

public class ExtensionVO {
	private int extension_seq;
	private String extension_name;
	private String extension_type;
	private String use_yn;
	
	public int getExtension_seq() {
		return extension_seq;
	}

	public void setExtension_seq(int extension_seq) {
		this.extension_seq = extension_seq;
	}

	public String getExtension_name() {
		return extension_name;
	}

	public void setExtension_name(String extension_name) {
		this.extension_name = extension_name;
	}

	public String getExtension_type() {
		return extension_type;
	}

	public void setExtension_type(String extension_type) {
		this.extension_type = extension_type;
	}

	public String getUse_yn() {
		return use_yn;
	}

	public void setUse_yn(String use_yn) {
		this.use_yn = use_yn;
	}
	
}
